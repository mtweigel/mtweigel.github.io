    // Method used to generate the GUI gameboard visual state
    private void generateGameState(int difficulty) {
        // Setting timer
        resetTimer();
        startTimer();
        populateLeaderboard();

        // Setting button visibility and highscore textbox to blank string
        checkSolutionButton.setVisible(true);
        showSolutionButton.setVisible(true);
        gameboardBannerLabel.setText("Play Sudoku!");
        gameboardBannerLabel.setForeground(Color.white);
        guiMessageLabel.setText("");

        // Setting all JTextField background colors to white
        for (int i = 0; i < positions.size(); i++) {
            positions.get(i).setBackground(Color.white);
        }

        // Checking if selected difficulty has remaining boards or not
        if (SelectDifficulty.checkDifficulty(difficulty)) {
            resetTimer();
            if (SelectDifficulty.allBoardsUsed == true) {
                for (int i = 0; i < positions.size(); i++) {
                    positions.get(i).setText("");;
                }
                easyButton.setEnabled(false);
                mediumButton.setEnabled(false);
                hardButton.setEnabled(false);
                guiMessageLabel.setText(SelectDifficulty.getGuiMessage());
            } else {
                switch (difficulty) {
                    case 1:
                        easyButton.setEnabled(false);
                        guiMessageLabel.setVisible(true);
                        guiMessageLabel.setText(SelectDifficulty.getGuiMessage());
                        break;
                    case 2:
                        mediumButton.setEnabled(false);
                        guiMessageLabel.setVisible(true);
                        guiMessageLabel.setText(SelectDifficulty.getGuiMessage());
                        break;
                    case 3:
                        hardButton.setEnabled(false);
                        guiMessageLabel.setVisible(true);
                        guiMessageLabel.setText(SelectDifficulty.getGuiMessage());
                        break;
                    default:
                        break;
                }
            }
        } else {
            gameboard.loadBoard(SelectDifficulty.getBoard());
            gameboard.generateSolved();

            gameboard.print(gameboard.getBoard());
            System.out.println("\n");
            gameboard.print(gameboard.getSolvedBoard());

            // Declaring variables
            int[][] game = gameboard.getBoard();
            int counter = 0;

            // Loop used to take gameboard array info and populate gameboard
            for (int j = 0; j < 9; j++) {
                for (int i = 0; i < 9; i++) {
                    if (game[j][i] == 0) {
                        positions.get(counter).setText("");
                        positions.get(counter).setEditable(true);
                    } else {
                        positions.get(counter).setText("" + game[j][i]);
                        positions.get(counter).setEditable(false);
                    }

                    counter++;
                }
            }
        }
    }

