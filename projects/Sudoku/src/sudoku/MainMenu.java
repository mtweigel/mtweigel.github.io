    private void checkSolutionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkSolutionButtonActionPerformed
        // Declaring variables
        boolean hasBeenSolved = true;
        int[][] game = gameboard.getBoard();
        int[][] solvedGame = gameboard.getSolvedBoard();
        int counter = 0;

        // Stopping game timer
        stopTimer();

        // Loop used to check mark incorrect integer entries
        for (int j = 0; j < 9; j++) {
            for (int i = 0; i < 9; i++) {
                // If statement used to check if anything has been entered into
                // the gameboard by the user
                if (positions.get(counter).getText().equals("")) {
                    hasBeenSolved = false;
                }

                // If statement used to highlight any incorrect user entries
                // on the gameboard to red
                if (game[j][i] == 0) {
                    if (!positions.get(counter).getText().equals("" + solvedGame[j][i]) && !positions.get(counter).getText().equals("")) {
                        positions.get(counter).setBackground(Color.red);
                        hasBeenSolved = false;
                    }
                }
                counter++;
            }
        }

        // Checking if gameboard has been solved, display message banner with
        // game results
        if (hasBeenSolved) {
            gameboardBannerLabel.setText("Successful Sudoku!");
            gameboardBannerLabel.setForeground(Color.green);
            checkSolutionButton.setVisible(false);
            playerTime = (elapsedTime / 1000);

            // Checks if solved board qualifies for a new highscore
            if (LeaderBoardScore.checkScore(SelectDifficulty.boardDifficulty, playerTime)) {
                newHighscorePanel.setVisible(true);
            }
        } else {
            gameboardBannerLabel.setText("Failed to Sudoku!");
            gameboardBannerLabel.setForeground(Color.red);
            checkSolutionButton.setVisible(false);
        }

        // Removing used gameboard
        SelectDifficulty.removeBoard();
    }//GEN-LAST:event_checkSolutionButtonActionPerformed

    private void showSolutionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showSolutionButtonActionPerformed
        // Declaring variables
        int[][] game = gameboard.getBoard();
        int[][] solvedGame = gameboard.getSolvedBoard();
        int counter = 0;

        // Stopping game timer
        stopTimer();

        // Setting anti-cheat provision by taking check solution button away
        // after clicking show solution button to see the solved sudoku board
        checkSolutionButton.setVisible(false);

        // Setting all JTextField background colors to white
        for (int j = 0; j < 9; j++) {
            for (int i = 0; i < 9; i++) {
                // If statement used to highlight any incorrect user entries
                // on the gameboard to red
                if (game[j][i] == 0) {
                    if (!positions.get(counter).getText().equals("" + solvedGame[j][i]) && !positions.get(counter).getText().equals("")) {
                        positions.get(counter).setBackground(Color.yellow);
                    }

                }
                counter++;
            }
        }

        // Resetting counter to populate board with answers
        counter = 0;

        // Loop used to populate correct answers to gameboard on GUI
        for (int j = 0; j < 9; j++) {
            for (int i = 0; i < 9; i++) {

                positions.get(counter).setText("" + solvedGame[j][i]);
                counter++;
            }
        }

        // Removing used gameboard
        SelectDifficulty.removeBoard();
    }//GEN-LAST:event_showSolutionButtonActionPerformed

